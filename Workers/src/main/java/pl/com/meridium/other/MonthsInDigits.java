package pl.com.meridium.other;

public class MonthsInDigits {
	
	public static String MonthInDigits(int month) {
		
		String m;
		switch (month) {
	    case 0:
	    	m="01";
	    	
	    	break;
	    case 1:
	    	m="02";
	    	
	    	break;
	    case 2:
	    	m="03";
	    	
	    	break;
	    case 3:
	    	m="04";
	    	
	    	break;
	    case 4:
	    	m="05";
	    	
	    	break;
	    case 5:
	    	m="06";
	    	
	    	break;
	    case 6:
	    	m="07";
	    	
	    	break;
	    case 7:
	    	m="08";
	    	
	    	break;
	    case 8:
	    	m="09";
	    	
	    	break;
	    case 9:
	    	m="10";
	    	
	    	break;
	    case 10:
	    	m="11";
	    	
	    	break;
	    case 11:
	    	m="12";
	    	
	    	break;
	    	default:
	    		m="00";
	    		
		    	break;	
	    	
	    }
		return m;
	}
	
	
    
    
}
